function v=getgs(k)
%% gauss coeficient
% 输入:
% k:每个方向的高斯点数
% 输出:
% v(1,i):第i个高斯点的位置；v(2,i):第i个高斯点的积分权重。

switch(k)
    case(1)
        v(1,1)=0;
        v(2,1)=2;
    case(2)
        v(1,1)=-0.577350269189626;
        v(1,2)=0.577350269189626;
        v(2,1)=1;
        v(2,2)=1;
    case(3)
        v(1,1)=-0.774596669241483;
        v(1,2)=0;
        v(1,3)=0.774596669241483;
        v(2,1)=0.555555555555556;
        v(2,2)=0.888888888888889;
        v(2,3)=0.555555555555556;
    case(4)
        v(1,1)=-0.861136311594053;
        v(1,2)=-0.339981043584856;
        v(1,3)=0.339981043584856;
        v(1,4)=0.861136311594053;
        v(2,1)=0.347854845137454;
        v(2,2)=0.652145154862546;
        v(2,3)=0.652145154862546;
        v(2,4)=0.347854845137454;
end